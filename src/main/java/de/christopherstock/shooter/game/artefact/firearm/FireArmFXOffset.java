
    package de.christopherstock.shooter.game.artefact.firearm;

    /**************************************************************************************
    *   The offset for the muzzle flash.
    *
    *   @author     Christopher Stock
    *   @version    0.3.11
    **************************************************************************************/
    public final class FireArmFXOffset
    {
        public int x = 0;
        public int y = 0;

        public FireArmFXOffset( int aX, int aY )
        {
            x = aX;
            y = aY;
        }
    }
